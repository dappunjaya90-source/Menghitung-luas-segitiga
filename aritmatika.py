c = 5
d = 10

# Penjumlahan
hasil_penjumlahan = c+d
print("Hasil penjumlahan angka: {}".format(hasil_penjumlahan))

# Pengurangan
hasil_pengurangan = c-d
print("Hasil pengurangan angka: {}".format(hasil_pengurangan))

# Perkalian
hasil_perkalian = c*d
print("Hasil perkalian angka: {}".format(hasil_perkalian))

# Pembagian
hasil_pembagian = c/d
print("Hasil pembagian angka: {}".format(hasil_pembagian))

# Perbandingan
hasil_perbandingan = c==d
print("Hasil perbandingan angka: {}".format(hasil_perbandingan))

# Tidak sama dengan
hasil_tidak_sama_dengan = c!=d
print("Hasil tidak sama dengan angka: {}".format(hasil_tidak_sama_dengan))


# Lebih besar dari
hasil_lebih_besar_dari = c>d
print(c>d)

# Lebih kecil dari
hasil_lebih_kecil_dari = c<d
print(c<d)

# Lebih besar sama dengan
hasil_lebih_besar_sama_dengan = c>=d
print(c>=d)

# Lebih kecil sama dengan
hasil_lebih_kecil_sama_dengan = c>=d
print(c<=d)